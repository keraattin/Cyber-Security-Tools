#!/usr/bin/env python3


# Imports
##############################################################################
import subprocess
##############################################################################


# Global Values
##############################################################################
IFACE       = "eth0"
NEW_MAC     = "00:11:22:33:44:55"
##############################################################################


# Functions
##############################################################################
def change_mac_addr():
    # Down Interface
    subprocess.call(['ifconfig','{}'.format(IFACE),'down'])

    # Change Hw Adress
    subprocess.call(['ifconfig',
                    '{}'.format(IFACE),
                    'hw',
                    'ether',
                    '{}'.format(NEW_MAC)])
    
    # Up Interface
    subprocess.call(['ifconfig','{}'.format(IFACE),'up'])
##############################################################################


# Main
##############################################################################
if __name__ == '__main__':
    change_mac_addr()
##############################################################################