#!/usr/bin/env python3


# Imports
##############################################################################
import re
##############################################################################


# Global Values
##############################################################################
IP_PATTERN = ("^(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)"
                    "\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)"
                    "\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)"
                    "\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$")
##############################################################################


# Validate Target IP Address
##############################################################################
def validate_ip_addr(ip_addr):
    ip_matches = re.search(IP_PATTERN,ip_addr)
    if ip_matches:
        return True
    else:
        return False
