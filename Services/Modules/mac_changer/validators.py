#!/usr/bin/env python3


# Imports
##############################################################################
import re
##############################################################################


# Global Values
##############################################################################
MAC_PATTRN = ("^([0-9a-fA-F]{2}:){5}[0-9a-fA-F]{2}$")
##############################################################################


# Validate Mac Address
##############################################################################
def validate_mac_addr(mac_addr):
    matches = re.search(MAC_PATTRN,mac_addr)
    if matches:
        return True
    else:
        return False
